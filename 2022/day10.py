from aoc_utilities import get_instructions
from pathlib import Path


def run_instructions(instructions):
    cycle = 0
    x = 1
    log = []
    countdown = 0
    buff = 0
    pixel = 0
    output = ''
    while cycle < 240:
        if countdown == 0:
            inst = next(instructions)
            x += buff
            sprite = set(range(x-1, x+2))
            if inst == 'noop':
                countdown = 1
                buff = 0
            else:
                countdown = 2
                buff = int(inst.split()[-1])

        if pixel in sprite:
            output += '#'
        else:
            output += '.'

        cycle += 1
        countdown -= 1
        pixel += 1
        if (cycle - 20) % 40 == 0:
            log.append(x * cycle)

        if cycle % 40 == 0:
            output += '\n'
            pixel = 0

    return log, output


def get_answer(data, part2=False):
    log, output = run_instructions(iter(data))
    print(sum(log))
    return output


